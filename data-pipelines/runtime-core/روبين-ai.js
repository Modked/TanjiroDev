    async function luminsesi(q, username, logic) {
        try {
            const response = await axios.post("https://luminai.my.id", {
                content: q,
                user: username,
                prompt: logic,
                webSearchMode: false
            });
            return response.data.result;
        } catch (error) {
            console.error('خطأ أثناء طلب الرد:', error);
            return "😂 حصلت مشكلة في الرد... جرب تاني!";
        }
    }

